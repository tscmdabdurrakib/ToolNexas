import { useState, useEffect } from "react";
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from "@/components/ui/card";
import { Textarea } from "@/components/ui/textarea";
import { Button } from "@/components/ui/button";
import { Label } from "@/components/ui/label";
import { Select, SelectContent, SelectItem, SelectTrigger, SelectValue } from "@/components/ui/select";
import { Copy, Type } from "lucide-react";
import { useToast } from "@/hooks/use-toast";

function ChangeTextFont() {
  const [inputText, setInputText] = useState<string>('Transform your text with different fonts!\nChoose from various Unicode fonts.');
  const [selectedFont, setSelectedFont] = useState<string>('bold');
  const [output, setOutput] = useState<string>('');
  const { toast } = useToast();

  const fontMappings = {
    bold: {
      name: "Bold",
      convert: (text: string) => text.replace(/[A-Za-z0-9]/g, (char) => {
        const bold = {
          'A': 'ğ€', 'B': 'ğ', 'C': 'ğ‚', 'D': 'ğƒ', 'E': 'ğ„', 'F': 'ğ…', 'G': 'ğ†', 'H': 'ğ‡', 'I': 'ğˆ',
          'J': 'ğ‰', 'K': 'ğŠ', 'L': 'ğ‹', 'M': 'ğŒ', 'N': 'ğ', 'O': 'ğ', 'P': 'ğ', 'Q': 'ğ', 'R': 'ğ‘',
          'S': 'ğ’', 'T': 'ğ“', 'U': 'ğ”', 'V': 'ğ•', 'W': 'ğ–', 'X': 'ğ—', 'Y': 'ğ˜', 'Z': 'ğ™',
          'a': 'ğš', 'b': 'ğ›', 'c': 'ğœ', 'd': 'ğ', 'e': 'ğ', 'f': 'ğŸ', 'g': 'ğ ', 'h': 'ğ¡', 'i': 'ğ¢',
          'j': 'ğ£', 'k': 'ğ¤', 'l': 'ğ¥', 'm': 'ğ¦', 'n': 'ğ§', 'o': 'ğ¨', 'p': 'ğ©', 'q': 'ğª', 'r': 'ğ«',
          's': 'ğ¬', 't': 'ğ­', 'u': 'ğ®', 'v': 'ğ¯', 'w': 'ğ°', 'x': 'ğ±', 'y': 'ğ²', 'z': 'ğ³',
          '0': 'ğŸ', '1': 'ğŸ', '2': 'ğŸ', '3': 'ğŸ‘', '4': 'ğŸ’', '5': 'ğŸ“', '6': 'ğŸ”', '7': 'ğŸ•', '8': 'ğŸ–', '9': 'ğŸ—'
        };
        return bold[char] || char;
      })
    },
    italic: {
      name: "Italic",
      convert: (text: string) => text.replace(/[A-Za-z]/g, (char) => {
        const italic = {
          'A': 'ğ´', 'B': 'ğµ', 'C': 'ğ¶', 'D': 'ğ·', 'E': 'ğ¸', 'F': 'ğ¹', 'G': 'ğº', 'H': 'ğ»', 'I': 'ğ¼',
          'J': 'ğ½', 'K': 'ğ¾', 'L': 'ğ¿', 'M': 'ğ‘€', 'N': 'ğ‘', 'O': 'ğ‘‚', 'P': 'ğ‘ƒ', 'Q': 'ğ‘„', 'R': 'ğ‘…',
          'S': 'ğ‘†', 'T': 'ğ‘‡', 'U': 'ğ‘ˆ', 'V': 'ğ‘‰', 'W': 'ğ‘Š', 'X': 'ğ‘‹', 'Y': 'ğ‘Œ', 'Z': 'ğ‘',
          'a': 'ğ‘', 'b': 'ğ‘', 'c': 'ğ‘', 'd': 'ğ‘‘', 'e': 'ğ‘’', 'f': 'ğ‘“', 'g': 'ğ‘”', 'h': 'â„', 'i': 'ğ‘–',
          'j': 'ğ‘—', 'k': 'ğ‘˜', 'l': 'ğ‘™', 'm': 'ğ‘š', 'n': 'ğ‘›', 'o': 'ğ‘œ', 'p': 'ğ‘', 'q': 'ğ‘', 'r': 'ğ‘Ÿ',
          's': 'ğ‘ ', 't': 'ğ‘¡', 'u': 'ğ‘¢', 'v': 'ğ‘£', 'w': 'ğ‘¤', 'x': 'ğ‘¥', 'y': 'ğ‘¦', 'z': 'ğ‘§'
        };
        return italic[char] || char;
      })
    },
    monospace: {
      name: "Monospace",
      convert: (text: string) => text.replace(/[A-Za-z0-9]/g, (char) => {
        const mono = {
          'A': 'ğ™°', 'B': 'ğ™±', 'C': 'ğ™²', 'D': 'ğ™³', 'E': 'ğ™´', 'F': 'ğ™µ', 'G': 'ğ™¶', 'H': 'ğ™·', 'I': 'ğ™¸',
          'J': 'ğ™¹', 'K': 'ğ™º', 'L': 'ğ™»', 'M': 'ğ™¼', 'N': 'ğ™½', 'O': 'ğ™¾', 'P': 'ğ™¿', 'Q': 'ğš€', 'R': 'ğš',
          'S': 'ğš‚', 'T': 'ğšƒ', 'U': 'ğš„', 'V': 'ğš…', 'W': 'ğš†', 'X': 'ğš‡', 'Y': 'ğšˆ', 'Z': 'ğš‰',
          'a': 'ğšŠ', 'b': 'ğš‹', 'c': 'ğšŒ', 'd': 'ğš', 'e': 'ğš', 'f': 'ğš', 'g': 'ğš', 'h': 'ğš‘', 'i': 'ğš’',
          'j': 'ğš“', 'k': 'ğš”', 'l': 'ğš•', 'm': 'ğš–', 'n': 'ğš—', 'o': 'ğš˜', 'p': 'ğš™', 'q': 'ğšš', 'r': 'ğš›',
          's': 'ğšœ', 't': 'ğš', 'u': 'ğš', 'v': 'ğšŸ', 'w': 'ğš ', 'x': 'ğš¡', 'y': 'ğš¢', 'z': 'ğš£',
          '0': 'ğŸ¶', '1': 'ğŸ·', '2': 'ğŸ¸', '3': 'ğŸ¹', '4': 'ğŸº', '5': 'ğŸ»', '6': 'ğŸ¼', '7': 'ğŸ½', '8': 'ğŸ¾', '9': 'ğŸ¿'
        };
        return mono[char] || char;
      })
    }
  };

  useEffect(() => {
    if (!inputText.trim()) {
      setOutput('');
      return;
    }

    const fontConverter = fontMappings[selectedFont];
    if (fontConverter) {
      setOutput(fontConverter.convert(inputText));
    }
  }, [inputText, selectedFont]);

  const copyToClipboard = async () => {
    if (!output) return;
    
    try {
      await navigator.clipboard.writeText(output);
      toast({
        title: "Copied!",
        description: "Styled text copied to clipboard",
      });
    } catch (err) {
      toast({
        title: "Failed to copy",
        description: "Please try again",
        variant: "destructive",
      });
    }
  };

  return (
    <div className="max-w-4xl mx-auto space-y-6">
      <Card className="border-2 shadow-lg">
        <CardHeader className="text-center space-y-2 pb-4 bg-gradient-to-r from-blue-50 to-indigo-50 dark:from-blue-950/20 dark:to-indigo-950/20">
          <div className="flex items-center justify-center gap-2">
            <Type className="h-6 w-6 text-blue-600 dark:text-blue-400" />
            <CardTitle className="text-2xl">Change Text Font</CardTitle>
          </div>
          <CardDescription>Transform your text with different Unicode font styles</CardDescription>
        </CardHeader>
        <CardContent className="pt-6 space-y-4">
          <div>
            <Label htmlFor="input-text">Input Text:</Label>
            <Textarea
              id="input-text"
              data-testid="input-text"
              placeholder="Enter your text here..."
              value={inputText}
              onChange={(e) => setInputText(e.target.value)}
              className="min-h-32 mt-2"
            />
          </div>

          <div>
            <Label htmlFor="font-style">Font Style:</Label>
            <Select value={selectedFont} onValueChange={setSelectedFont}>
              <SelectTrigger className="mt-2">
                <SelectValue />
              </SelectTrigger>
              <SelectContent>
                {Object.entries(fontMappings).map(([key, font]) => (
                  <SelectItem key={key} value={key}>
                    {font.name}
                  </SelectItem>
                ))}
              </SelectContent>
            </Select>
          </div>

          <div>
            <Label htmlFor="output">Styled Text:</Label>
            <Textarea
              id="output"
              data-testid="output-result"
              value={output}
              readOnly
              className="min-h-32 mt-2 bg-gray-50 dark:bg-gray-900"
              placeholder="Styled text will appear here..."
            />
          </div>

          <div className="bg-blue-50 dark:bg-blue-950/20 p-3 rounded-lg">
            <p className="text-sm text-blue-700 dark:text-blue-300">
              <strong>Example:</strong> Transform "Hello" to "ğ‡ğğ¥ğ¥ğ¨" (Bold), "ğ»ğ‘’ğ‘™ğ‘™ğ‘œ" (Italic), or "ğ™·ğšğš•ğš•ğš˜" (Monospace)
            </p>
          </div>

          <Button
            onClick={copyToClipboard}
            data-testid="button-copy"
            className="w-full"
            disabled={!output}
          >
            <Copy className="mr-2 h-4 w-4" />
            Copy Result
          </Button>
        </CardContent>
      </Card>
    </div>
  );
}

export default ChangeTextFont;